    private void sAddButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_sAddButton1ActionPerformed
        // TODO add your handling code here:
        String name=sNameTextField.getText();
        String contact = sContactTextField.getText();
        String email = sEmailTextField.getText();
        if(!name.equals(null) && !contact.equals(null) && !email.equals(null)){
            AddSupplier aAddSupplier = new AddSupplier();
            try {
                if (aAddSupplier.AddNewSupplier(name, contact, email)){
                    JOptionPane.showMessageDialog(null, "New supplier added successfully");
                }
            } catch (SQLException ex) {
                Logger.getLogger(SupplierUpdate.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        else
            JOptionPane.showMessageDialog(null, "Enter the supplier info!!!");
    }//GEN-LAST:event_sAddButton1ActionPerformed

