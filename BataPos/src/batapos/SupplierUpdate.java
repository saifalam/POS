    private void sAddButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_sAddButtonActionPerformed
        // TODO add your handling code here:
       String id= sIdTextField.getText();
       if(!id.equals(null)){
           RemoveSupplier aRemoveSupplier =new RemoveSupplier();
            try {
                if(aRemoveSupplier.SupplierRemove(Integer.parseInt(id))){
                    JOptionPane.showMessageDialog(null, "Supplier removed successfulyy!!!");
                }
            } catch (SQLException ex) {
                Logger.getLogger(SupplierUpdate.class.getName()).log(Level.SEVERE, null, ex);
            }
       }
       else
           JOptionPane.showMessageDialog(null, "enter the supplier id");
    }//GEN-LAST:event_sAddButtonActionPerformed

